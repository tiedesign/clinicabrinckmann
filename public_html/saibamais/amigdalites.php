    							e essa inflama&ccedil;&atilde;o provocada pelo v&iacute;rus pode ter facilitado a penetra&ccedil;&atilde;o de bact&eacute;rias  da flora comum da faringe.
    						</p>
    
    						<p>
    							E o v&iacute;rus? Quando penetra? Acredita-se que o v&iacute;rus penetre mais facilmente nas am&iacute;gdalas quando a temperatura ambiente est&aacute; baixa, pois perdemos alguns dos mecanismos de defesa da mucosa respirat&oacute;ria, tais como os movimentos ciliares. 
