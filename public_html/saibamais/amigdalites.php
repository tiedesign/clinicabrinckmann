    					        porque bact&eacute;rias e v&iacute;rus se acumulam nessas dobras que n&atilde;o est&atilde;o mais produzindo anticorpos e causam infec&ccedil;&otilde;es de repeti&ccedil;&atilde;o. 
    							Nesses casos, a retirada das am&iacute;gdalas eliminam as infec&ccedil;&otilde;es.
