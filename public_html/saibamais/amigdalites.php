    							Juntas, as am&iacute;gdalas palatinas, linguais, a aden&oacute;ide e demais tecidos linf&oacute;ides da faringe formam o Anel  de Waldeyer, que tem a fun&ccedil;&atilde;o de produzir anticorpos, protegendo esta regi&atilde;o de infec&ccedil;&otilde;es. 
    							Esses tecidos linf&oacute;ides s&atilde;o muito grandes nas crian&ccedil;as e tendem a diminuir com a idade.
    						</p>
    
    						<p>
    							Quando h&aacute; infec&ccedil;&otilde;es nas am&iacute;gdalas por bact&eacute;rias ou v&iacute;rus, ocorre a chamada amigdalite. 
