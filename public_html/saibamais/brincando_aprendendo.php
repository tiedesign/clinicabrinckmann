                            <p class="subtitle">Brincando e Aprendendo &ndash; Como Estimular a Linguagem e a Fala de Seu Filho</p>

							<p>
								Seu/sua filho/a est&aacute; apresentando algumas trocas na fala, como por exemplo: &#8220;tof&aacute;&#8221; ao inv&eacute;s de sof&aacute;; &#8220;tama&#8221; ao inv&eacute;s de cama? 
