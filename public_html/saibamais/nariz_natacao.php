                            <p class="subtitle">O nariz e a nata&ccedil;&atilde;o</p>
                            
							<p>Para a maioria das crian&ccedil;as a nata&ccedil;&atilde;o &eacute; um excelente esporte!</p>

							<p>&Eacute; importante lembrar que epis&oacute;dios isolados de sinusite n&atilde;o contraindicam a nata&ccedil;&atilde;o.</p>

							<p>A nata&ccedil;&atilde;o deve ser suspensa apenas durante o tratamento da sinusite ou em alguns casos de rinite.</p>

							<p>A nata&ccedil;&atilde;o &eacute; contraindicada para crian&ccedil;as com sinusites de repeti&ccedil;&atilde;o, que n&atilde;o apresentam melhora com os tratamentos tradicionais e que pioram quando nadam.</p>

							<p>Algumas crian&ccedil;as com rinite ficam com o nariz entupido ap&oacute;s nadar e devem ser avaliadas pelo m&eacute;dico. Certos cuidados especiais podem ser realizados, como a limpeza das fossas nasais com soro fisiol&oacute;gico ap&oacute;s a nata&ccedil;&atilde;o.</p>

							<p>Deve-se avaliar a possibilidade de a crian&ccedil;a inspirar &aacute;gua durante a pr&aacute;tica do esporte, o que acontece especialmente com aquelas que est&atilde;o aprendendo a nadar.</p>

							<p>Algumas vezes os sintomas de disfun&ccedil;&atilde;o nasal (nariz entupido ou cheio de catarro) somente ocorrem quando a piscina est&aacute; num ambiente com ventila&ccedil;&atilde;o inadequada.</p>

							<p>O cloro pode irritar o nariz das crian&ccedil;as mais sens&iacute;veis. Nesses casos, piscinas salinizadas ou ozonizadas s&atilde;o as indica&ccedil;&otilde;es mais saud&aacute;veis.</p>
							
