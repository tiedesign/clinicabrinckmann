                            <p class="subtitle">Por que os idosos caem?</p>
                            
    						<p>
    							Sabemos que as quedas ocupam o primeiro lugar nas causas de acidentes com pessoas idosas, mesmo as saud&aacute;veis. Isso quer dizer que n&atilde;o precisa haver doen&ccedil;a, mas apenas o envelhecimento normal do nosso sistema de equil&iacute;brio justifica a tend&ecirc;ncia para cair.
    						</p>
    
    						<p>
    							Com o passar dos anos, nosso organismo vai perdendo naturalmente a capacidade de responder a alguns est&iacute;mulos essenciais para nosso equil&iacute;brio como, por exemplo, virar rapidamente a cabe&ccedil;a quando ouvimos algum ru&iacute;do atr&aacute;s de n&oacute;s. A essa capacidade, chamamos de reflexo, ou seja: um est&iacute;mulo (a audi&ccedil;&atilde;o) que desencadeia uma resposta (girar a cabe&ccedil;a). Para que o reflexo aconte&ccedil;a, precisamos boa qualidade na capta&ccedil;&atilde;o e resposta ao mesmo tempo.
    						</p>
    
    						<p>
    							Para o equil&iacute;brio, dependemos essencialmente de nossa vis&atilde;o, do labirinto e da estabilidade de nosso sistema locomotor (articula&ccedil;&otilde;es e m&uacute;sculos), al&eacute;m da integra&ccedil;&atilde;o perfeita entre todos eles, que &eacute; realizada pelo sistema nervoso. Com a idade, aparecem os problemas de vis&atilde;o (catarata, necessidade de &oacute;culos, doen&ccedil;as na retina), articula&ccedil;&otilde;es (artrose, osteoporose, reumatismo), labirinto (surdez e doen&ccedil;as do equil&iacute;brio), e do nosso sistema nervoso (perdemos a capacidade de conduzir as informa&ccedil;&otilde;es com rapidez e qualidade). O que fazer?
