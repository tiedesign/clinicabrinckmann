                            <p class="subtitle">Cera no ouvido</p>
                            
							<p>Acera &eacute; o produto de secre&ccedil;&atilde;o das gl&acirc;ndulas que se localizam na parte mais externa do canal do ouvido (conduto auditivo externo). Sua fun&ccedil;&atilde;o &eacute; a de proteger a pele desse conduto, mantendo-a hidratada e impermeabilizada, e tamb&eacute;m funciona como um ve&iacute;culo para facilitar a sa&iacute;da de restos de pele e de outros detritos que porventura possam entrar no conduto auditivo. A colora&ccedil;&atilde;o da cera varia do amarelo at&eacute; o marrom-escuro. A consist&ecirc;ncia tamb&eacute;m difere de crian&ccedil;a para crian&ccedil;a.</p>

							<p>A cera n&atilde;o &eacute; um problema, mas o seu excesso, sim.</p>

							<p>&Eacute; importante frisar que o ouvido &eacute; "auto-limpante", por isso n&atilde;o &eacute; preciso utilizar objetos com o intuito de "ajudar" a natureza. Lembre-se do velho dito popular: "S&oacute; se co&ccedil;a o ouvido com o cotovelo".</p>

							<p>O uso de hastes de pl&aacute;stico com algod&atilde;o nas extremidades pode "empurrar" a cera para o interior do canal auditivo ao inv&eacute;s de retir&aacute;-la. Em caso de necessidade, seu m&eacute;dico otorrino estar&aacute; apto para fazer a limpeza do conduto auditivo da crian&ccedil;a usando irriga&ccedil;&atilde;o ou uma delicada cureta ou pin&ccedil;a. Em geral, esse processo &eacute; indolor e r&aacute;pido.</p>

							<p>As m&atilde;es podem utilizar a ponta da toalha para limpar externamente a orelha da crian&ccedil;a.</p>
							
