                            <p class="subtitle">Aden&oacute;ides</p>
                            
    						<p>
    							As  Aden&oacute;ides, tamb&eacute;m conhecidas por "carne esponjosa", s&atilde;o uma massa de tecido linf&oacute;ide que ocupa o teto da nasofaringe. Juntamente com as am&iacute;gdalas palatinas e as linguais, as aden&oacute;ides comp&otilde;em o Anel Linf&aacute;tico de Waldeyer. Sua fun&ccedil;&atilde;o primordial &eacute; estimular a resposta imune - anticorpos - a v&aacute;rios ant&iacute;genos ingeridos.
    						</p>
    
    						<p>
    							O tecido linf&oacute;ide do Anel Linf&aacute;tico de Waldeyer pode se encontrar aumentado principalmente por volta dos 05 a 07 anos de idade, sendo que normalmente regridem durante a inf&acirc;ncia, tornando rara a sua observa&ccedil;&atilde;o em adultos.
    						</p>
    
    						<p>
                                O aumento da aden&oacute;ide &eacute; uma causa freq&uuml;ente de obstru&ccedil;&atilde;o nasal e respira&ccedil;&atilde;o bucal cr&ocirc;nica, podendo ainda contribuir na piora dos roncos noturnos. Pode ocorrer, ainda, epis&oacute;dio de otite m&eacute;dia por obstru&ccedil;&atilde;o da tuba auditiva, localizada ao lado da aden&oacute;ide. As indica&ccedil;&otilde;es para adenoidectomia (cirurgia para retirada das aden&oacute;ides) s&atilde;o: obstru&ccedil;&atilde;o nasal causada por aumento da aden&oacute;ide, respira&ccedil;&atilde;o bucal cr&ocirc;nica, roncos noturnos, dificuldade de degluti&ccedil;&atilde;o e altera&ccedil;&otilde;es no desenvolvimento da fala sem outras causas atribu&iacute;veis. H&aacute;, tamb&eacute;m, indica&ccedil;&otilde;es cir&uacute;rgicas por motivo de infec&ccedil;&atilde;o recorrente da aden&oacute;ide (adenoidite) ou otites m&eacute;dias recorrentes.
    						</p>
    
    						<p>
    							A remo&ccedil;&atilde;o cir&uacute;rgica &eacute; curativa para a hipertrofia da aden&oacute;ide, por&eacute;m &eacute; importante ressaltar que n&atilde;o evita todos os epis&oacute;dios futuros de infec&ccedil;&otilde;es virais (resfriados e gripes).
