                            <p class="subtitle">Cirurgia de Am&iacute;gdalas e Aden&oacute;ides</p>
                            
    						<p>
    							No Brasil, s&atilde;o realizadas em torno de 200.000 cirurgias de am&iacute;gdalas e aden&oacute;ides. 
    							Estas cirurgias n&atilde;o s&atilde;o t&atilde;o indicadas como no passado, quando ainda n&atilde;o existiam antibi&oacute;ticos t&atilde;o eficazes, mas s&atilde;o cirurgias que melhoram muito a sa&uacute;de de crian&ccedil;as e adultos quando devidamente propostas, ou seja, com indica&ccedil;&atilde;o precisa.
    						</p>
    
    						<p>
    							Am&iacute;gdalas e aden&oacute;ides s&atilde;o compostas de um tecido parecido com os dos linfonodos ou "g&acirc;nglios" que encontramos, &agrave;s vezes, no pesco&ccedil;o, axilas ou virilhas. 
    							Elas s&atilde;o parte de um "anel" de tecido glandular que envolve internamente a garganta, formado pelas am&iacute;gdalas palativas, am&iacute;gdalas linguais, am&iacute;gdalas far&iacute;ngeas (aden&oacute;ides) e tecido linf&oacute;ide espalhado pela faringe.
    						</p>
    
    						<p>
    							As aden&oacute;ides est&atilde;o localizadas na parte superior da garganta, no fundo do nariz. 
    							Portanto, dificultam a respira&ccedil;&atilde;o quando aumentadas ou posicionadas muito pr&oacute;ximas &agrave;s coanas (por&ccedil;&atilde;o final das fossas nasais).
    						</p>
    
    						<p>
    							As am&iacute;gdalas s&atilde;o duas massas de tecido localizadas de cada lado da garganta, no fundo da boca. 
    							Elas est&atilde;o estrategicamente localizadas perto das entradas do ar que respiramos e podem facilmente ser infectadas por v&iacute;rus ou bact&eacute;rias que v&ecirc;m com o ar. 
    							&Eacute; dessa forma que elas ajudam a formar anticorpos contra esses germes, como parte do nosso assim chamado "sistema imunol&oacute;gico". 
    							Assim, resistimos a futuras infec&ccedil;&otilde;es. 
    							Esta fun&ccedil;&atilde;o &eacute; realizada nos primeiros anos de vida e vai se tornando menos importante quando a crian&ccedil;a vai ficando mais velha.
    						</p>
    
    						<p>
    							N&atilde;o h&aacute; evid&ecirc;ncias de que as am&iacute;gdalas e aden&oacute;ides sejam importantes ap&oacute;s os 03 anos de idade. 
    							Estudos com milhares de crian&ccedil;as que fizeram cirurgias de am&iacute;gdalas mostraram que as mesmas sofreram menos infec&ccedil;&otilde;es em geral do que crian&ccedil;as que n&atilde;o foram operadas. 
    							Isso se explica porque, &agrave;s vezes, as am&iacute;gdalas e as aden&oacute;ides passam a se tornar focos de infec&ccedil;&atilde;o que agridem e debilitam o organismo.
    						</p>
    
    						<p>
    							As aden&oacute;ides grandes ocorrem quase que exclusivamente em crian&ccedil;as, pois ap&oacute;s a puberdade, normalmente, as aden&oacute;ides 
    							regridem de tamanho. De qualquer forma, &eacute; importante esclarecer que as 
    							aden&oacute;ides grandes causam obstru&ccedil;&atilde;o nasal e podem levar a crian&ccedil;a a dormir 
    							de boca aberta, roncando &agrave; noite e apresentando, com o passar do tempo, 
