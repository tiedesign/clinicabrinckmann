    					        em virtude da respira&ccedil;&atilde;o bucal. Isto pode levar a crian&ccedil;a a ter problemas 
    					        no ouvido (secre&ccedil;&otilde;es atr&aacute;s do t&iacute;mpano e infec&ccedil;&otilde;es no ouvido m&eacute;dio) e mais 
    					        problemas de infec&ccedil;&otilde;es de garganta e pulm&atilde;o.
