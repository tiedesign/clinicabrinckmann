                            <p class="subtitle">Altera&ccedil;&otilde;es Vocais do Beb&ecirc; ao Adolescente</p>
						
							<p><strong>Disfonia Infantil</strong></p>
							
							<p>
								As altera&ccedil;&otilde;es de voz na primeira inf&acirc;ncia rotineiramente n&atilde;o recebem a devida import&acirc;ncia por parte dos pais, professores e at&eacute; mesmo do m&eacute;dico pediatra. 
								A rouquid&atilde;o na crian&ccedil;a n&atilde;o &eacute; raz&atilde;o de preocupa&ccedil;&atilde;o para a popula&ccedil;&atilde;o em geral, inclusive para profissionais da sa&uacute;de. 
								As pesquisadoras Soares e Behlau (1998) realizaram um estudo sobre a vis&atilde;o do m&eacute;dico pediatra a respeito da disfonia infantil. 
								Foram entrevistados 133 pediatras e descobriu-se:
							</p>
						
							<ul>
								<li>Quanto aos m&eacute;dicos:	71,4% dos pediatras consideram a rouquid&atilde;o um sinal que mere&ccedil;a maior investiga&ccedil;&atilde;o; 27% acham que n&atilde;o &eacute; importante!; 0,8% acreditam que, &agrave;s vezes, &eacute; desnecess&aacute;ria uma investiga&ccedil;&atilde;o;</li>
								<li>Quanto aos pais: apenas 72,7% dos pais reconhecem a voz rouca dos filhos; 20% n&atilde;o reconhecem que o filho est&aacute; com problemas na voz e 6,8% reconhecem &agrave;s vezes; 3,8% consideram a rouquid&atilde;o uma caracter&iacute;stica positiva (equivocadamente).</li>
							</ul>
						
							<p>
								Nos primeiros anos de vida algumas doen&ccedil;as podem colocar em risco a vida de crian&ccedil;as que n&atilde;o recebam o devido acompanhamento m&eacute;dico. Estenose subgl&oacute;tica, laringomal&aacute;cia, papilomas e paralisias das pregas vocais s&atilde;o transtornos menos comuns, por&eacute;m podem obstruir as vias a&eacute;reas.
							</p>
						
							<p>	
								Os sintomas mais comuns s&atilde;o: dificuldade respirat&oacute;ria intermitente, altera&ccedil;&atilde;o vocal, anormalidade no som do choro, afonia (aus&ecirc;ncia de voz), cianose (ficar roxo), estridor (ru&iacute;do que ocorre na passagem do ar), dificuldade de alimenta&ccedil;&atilde;o, tosse, apn&eacute;ia e pneumonias de repeti&ccedil;&atilde;o. 
								A voz no beb&ecirc; pode ser considerada como um importante par&acirc;metro de sa&uacute;de ou doen&ccedil;a.
							</p>
						
							<p>
								Alguns pesquisadores acreditam que pode-se esperar pela puberdade, pois determinadas altera&ccedil;&otilde;es desaparecem com o crescimento e desenvolvimento da laringe. 
								No entanto, outros autores alertam  que existem altera&ccedil;&otilde;es benignas, como cistos em meninos, que durante a puberdade podem romper-se e originar o que chamamos de sulco vocal. 
								Em uma situa&ccedil;&atilde;o como est&aacute; o dano vocal ser&aacute; muito maior e o tratamento limitado, ficando o adolescente com a qualidade e o rendimento vocal seriamente comprometidos. 
								Devemos ainda dimensionar o dano emocional gerado por anos de voz deteriorada, o que geralmente traz estigmas &agrave; crian&ccedil;a e compromete suas habilidades de comunica&ccedil;&atilde;o e intera&ccedil;&atilde;o social.
							</p>
						
							<p>
								Dados indicam que 3 milh&otilde;es de crian&ccedil;as norte-americanas apresentam disfonias. 
								No Brasil, at&eacute; 23% das crian&ccedil;as em idade escolar podem apresentar alguma altera&ccedil;&atilde;o vocal, e 5% destas crian&ccedil;as apresentam problemas cr&ocirc;nicos, ou seja, de longos per&iacute;odos ou j&aacute; estabelecidos. 
								Este percentual pode variar de acordo com a cultura local, com o comportamento social mais expansivo ou menos expansivos de determinados grupos sociais. 
								Normalmente pensa-se em transtornos agudos, que passar&atilde;o em 10 ou 15 dias, por&eacute;m a literatura aponta que a grande maioria das patologias na inf&acirc;ncia ocorrem por mau uso e abuso vocal.
							</p>
						
							<p>
								As laringites e infec&ccedil;&otilde;es do trato respirat&oacute;rio ocasionam disfonias tempor&aacute;rias. 
								Traumas vocais com "incha&ccedil;o" dos tecidos ocorrem geralmente por gritos em jogos, falas com competi&ccedil;&atilde;o de ru&iacute;do como festas e intervalos escolares. 
								Os n&oacute;dulos vocais (popularmente chamados de calos) s&atilde;o os mais freq&uuml;entes e predominam no sexo masculino. 
								O mais evidente e importante sintoma &eacute; a voz rouca como indicador de desequil&iacute;brio do sistema respons&aacute;vel pela sonoridade vocal. 
								In&uacute;meros pesquisadores alertam que  todo caso de rouquid&atilde;o  prolongada  necessita de cuidados  m&eacute;dicos imediatos.
							</p>
						
							<p>
								As press&otilde;es da vida moderna e as exig&ecirc;ncias do sistema educacional propiciam o desenvolvimento de padr&otilde;es hipertensos de produ&ccedil;&atilde;o da voz e da fala em crian&ccedil;as. 
								Como outras causas sociais podemos citar os h&aacute;bitos familiares e modelos n&atilde;o adequados de voz e fala.
							</p>
						
							<p>
								Encontramos ainda problemas de voz associados aos desvios de fala (trocas e omiss&otilde;es de fonemas), problemas respirat&oacute;rios, altera&ccedil;&otilde;es de linguagem (dificuldade em expressar-se claramente ou em  compreender frases compostas), altera&ccedil;&otilde;es do comportamento (agita&ccedil;&atilde;o, desaten&ccedil;&atilde;o). 
								Nestas situa&ccedil;&otilde;es o problema de voz poder&aacute; passar desapercebido e n&atilde;o ser valorizado.
							</p>
						
							<p>
								Crian&ccedil;as com problemas vocais podem apresentar o que chamamos de altera&ccedil;&atilde;o do Processamento Auditivo Central ou em alguns casos podem apresentar D&eacute;ficit de Aten&ccedil;&atilde;o. 
								Estas desordens apresentam como sintomas a hiperatividade e o atraso na aquisi&ccedil;&atilde;o da fala. 
								O processamento auditivo central  &eacute; respons&aacute;vel pela aten&ccedil;&atilde;o seletiva, inibi&ccedil;&atilde;o dos ru&iacute;dos competitivos a uma mensagem auditiva, localiza&ccedil;&atilde;o da fonte sonora, lateraliza&ccedil;&atilde;o do som, discrimina&ccedil;&atilde;o auditiva  e aspectos temporais da audi&ccedil;&atilde;o.
							</p>
						
							<p>
								Como identificar a desordem do processamento auditivo central em seu filho? 
								Se 5 ou mais destes itens forem assinalados, voc&ecirc; deve procurar avalia&ccedil;&atilde;o m&eacute;dica e fonoaudiol&oacute;gica:
							</p>
						
							<ul>
								<li>Emiss&atilde;o de "&atilde;hn, como e o qu&ecirc;" por mais de 5 x ao dia;</li>
								<li>Dificuldades na produ&ccedil;&atilde;o de determinados sons da fala;</li>
								<li>Rapidamente esquecer o que foi falado;</li>
								<li>Falta de aten&ccedil;&atilde;o;</li>
								<li>Perda de audi&ccedil;&atilde;o;</li>
								<li>Dificuldade em manter a aten&ccedil;&atilde;o em locais ruidosos;</li>
								<li>Capacidade verbal menor que o desempenho global;</li>
								<li>Problemas ou dificuldades na escola;</li>
								<li>Dificuldade em realizar  comandos verbais;</li>
								<li>Trocas na leitura e na escrita;</li>
								<li>Hipo  ou hiperatividade;</li>
								<li>Pede para repetir o que foi falado constantemente;</li>
								<li>Dificuldades com os atos motores finos.</li>
							</ul>
						
							<p>
								<strong>Disfonia na Adolesc&ecirc;ncia</strong>
							</p>
						
							<p>
								&Eacute; com a puberdade que ocorre a muda vocal, per&iacute;odo fisiol&oacute;gico em que a voz de meninos e meninas sofre a transforma&ccedil;&atilde;o para a voz adulta, fazendo com que a voz fique mais grave. 
								Nas meninas este processo &eacute; mais sutil e gradativo. 
								No menino essa adapta&ccedil;&atilde;o funcional ocasionada pelo crescimento das estruturas lar&iacute;ngeas &eacute; mais desequilibrada, pois as estruturas crescem de forma n&atilde;o harm&ocirc;nica. 
								Este per&iacute;odo na menina ocorre entre 12 e 14 anos e nos meninos entre 13 e 15 anos, relacionado com o aumento da estatura f&iacute;sica, encerrando-se com o surgimento dos p&ecirc;los na face.
							</p>
						
							<p>
								Nesta etapa da vida o adolescente pode apresentar rouquid&atilde;o, diplofonia, voz &aacute;spera e soprosa. 
								A dura&ccedil;&atilde;o da muda vocal fisiol&oacute;gica deve durar de meses a 1 ano. 
								Nesta fase o canto torna-se dif&iacute;cil e deve ser evitado. 
								Per&iacute;odos de desajuste vocal maiores que 12 meses sugerem a necessidade de avalia&ccedil;&atilde;o m&eacute;dica. 
								Observa-se que 6% dos meninos entre  13 e 18 anos apresentam dist&uacute;rbios vocais. 
								Nas mulheres uma voz muito aguda ou infatilizada pode ser indicativo de muda vocal incompleta. 
								O aux&iacute;lio m&eacute;dico e fonoaudiol&oacute;gico poder&aacute; estabelecer o equil&iacute;brio vocal e adequar a voz do indiv&iacute;duo.
							</p>

