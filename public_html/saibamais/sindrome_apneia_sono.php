                            <p class="subtitle">S&iacute;ndrome da Apn&eacute;ia Obstrutiva do Sono</p>
                            
    						<p>
    							Todos n&oacute;s sabemos que uma boa noite de sono &eacute; fundamental para nossa sa&uacute;de.Mas n&atilde;o basta deitar e dormir a noite toda (sem fazer barulho...) para acordar bem no dia seguinte. 
    							Hoje em dia, &eacute; consenso entre os m&eacute;dicos que a qualidade do sono (e n&atilde;o apenas o n&uacute;mero de horas durante as quais o indiv&iacute;duo dorme) tamb&eacute;m &eacute; importante para um repouso adequado, repondo as energias gastas na rotina di&aacute;ria.
    						</p>
    
    						<p>
    							A cada parada da respira&ccedil;&atilde;o, a baixa de oxigena&ccedil;&atilde;o pode desencadear um sinal de alerta, ocasionando despertares r&aacute;pidos durante a noite, muitas vezes sequer percebidos pela pr&oacute;pria pessoa. 
    							Somando-se todos esses despertares, contudo, o tempo de sono eficiente &eacute; pequeno, e isso explica porque o indiv&iacute;duo j&aacute; acorda cansado.
    						</p>
    
    						<p>
    							Algumas pessoas sofrem com a S&iacute;ndrome da Apn&eacute;ia Obstrutiva do Sono - SAOS, que &eacute; a dificuldade para respirar enquanto se est&aacute; dormindo, 
    							podendo ser provocada por v&aacute;rios fatores como, por exemplo, altera&ccedil;&otilde;es anat&ocirc;micas (ocasionando obstru&ccedil;&atilde;o do nariz ou da garganta), 
    							altera&ccedil;&otilde;es posturais (dormir de barriga para cima), predisposi&ccedil;&atilde;o familiar, efeitos medicamentosos (sedativos, calmantes e outras drogas), 
    							maus h&aacute;bitos (fumar, beber &aacute;lcool no jantar, comer logo antes de dormir), aumento de peso e sedentarismo.
    						</p>
    
    						<p>
    							A baixa oxigena&ccedil;&atilde;o sang&uuml;&iacute;nea durante a noite pode afetar o aparelho cardiovascular, levando &agrave; hipertens&atilde;o arterial, arritmias card&iacute;acas, 
    							impot&ecirc;ncia sexual, ou o pr&oacute;prio funcionamento cerebral, prejudicando a concentra&ccedil;&atilde;o em determinadas atividades e a mem&oacute;ria. 
    							Para se ter uma id&eacute;ia da gravidade da s&iacute;ndrome, os pacientes com apn&eacute;ia sofrem acidentes de tr&acirc;nsito sete vezes mais do que a popula&ccedil;&atilde;o geral.
    						</p>
    
    						<p>
    							A S&iacute;ndrome da Apn&eacute;ia Obstrutiva do Sono pode ocorrer em qualquer faixa et&aacute;ria, com pico de incid&ecirc;ncia entre os 40 e 50 anos. 
    							A obesidade &eacute; o principal fator de risco para a s&iacute;ndrome, destacando-se que cerca de 2/3 dos pacientes com SAOS s&atilde;o obesos. 
    							Al&eacute;m disso, &eacute; poss&iacute;vel que existam fatores gen&eacute;ticos associados, o que explica o fato de v&aacute;rias pessoas da mesma fam&iacute;lia apresentarem tal quadro.
    						</p>
    						
    						<p>
    							Deve-se ficar atento para a possibilidade de haver S&iacute;ndrome da Apn&eacute;ia Obstrutiva do Sono, principalmente se a pessoa:
    						</p>
    
    						<ul>
    							<li>ronca;</li>
    							<li>&eacute; obesa ou hipertensa;</li>
    							<li>permanece com sonol&ecirc;ncia diurna, apesar de dormir v&aacute;rias horas por noite;</li>
    							<li>tem irritabilidade ou dificuldade para se concentrar em atividades habituais;</li>
    							<li>tem redu&ccedil;&atilde;o da libido ou impot&ecirc;ncia sexual;</li>
    							<li>levanta v&aacute;rias vezes durante a noite para urinar.</li>
    						</ul>
    						
    						<p>
    							<b>Tratamento</b>
    						</p>
                           
    						<p>
    							Perder peso, evitar ingest&atilde;o de bebidas alco&oacute;licas antes de dormir, ter um quarto silencioso e hor&aacute;rios regulares para dormir, em regra, s&atilde;o medidas suficientes para melhorar os quadros de apn&eacute;ia leve.
    						</p>
    					
    						<p>
    							Quando s&atilde;o diagnosticadas outras causas para apn&eacute;ia, como problemas anat&ocirc;micos das vias a&eacute;reas superiores, 
    							o m&eacute;dico pode indicar o uso de medicamentos, aparelhos especiais para manter a respira&ccedil;&atilde;o durante o sono 
    							(os CPAP's, sigla inglesa para aparelho de press&atilde;o positiva cont&iacute;nua) ou mesmo algum tipo de cirurgia, 
    							de acordo com a necessidade do paciente.
