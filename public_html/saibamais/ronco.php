                            <p class="subtitle">Ronco</p>
                            
    						<p>
    							Existem estat&iacute;sticas indicando que a popula&ccedil;&atilde;o entre 30 a 35 anos de idade, 20% dos homens e 5% das mulheres, ronca. 
    							Na faixa dos 60 anos de idade, mais da metade das pessoas roncam.
    						</p>
    
    						<p>
    							O ronco praticamente s&oacute; acontece na esp&eacute;cie humana, j&aacute; que a quase totalidade dos animais - exceto o homem - dorme em dec&uacute;bito ventral ou lateral, impedindo a queda da mand&iacute;bula em dire&ccedil;&atilde;o &agrave; garganta. 
    							Uma vez que os primatas adotaram o dec&uacute;bito dorsal (barriga para cima) para dormir, adquiriram a in&eacute;dita capacidade de roncar.
    						</p>
    
    						<p>
    							O ru&iacute;do caracter&iacute;stico do ronco durante o sono pode se originar em qualquer ponto da via a&eacute;rea, desde o nariz at&eacute; a epiglote, na entrada da laringe.
    						</p>
    
    						<p>
    							S&atilde;o causas do ronco:
    						</p>
    
    						<ul>
    							<li>Flacidez da musculatura do palato (c&eacute;u da boca), l&iacute;ngua e faringe, gerando a incapacidade de manter a via a&eacute;rea aberta durante a inspira&ccedil;&atilde;o. &Eacute; a principal causa do ronco na idade adulta.</li>
    							<li>Obstru&ccedil;&atilde;o respirat&oacute;ria pelo tamanho aumentado das am&iacute;gdalas, aden&oacute;ide, cistos, tumores, l&iacute;ngua, etc. Esta situa&ccedil;&atilde;o acaba comprometendo a passagem de ar.</li>
    							<li>Comprimento excessivo do palato mole e &uacute;vula (campainha), produzindo vibra&ccedil;&otilde;es durante a respira&ccedil;&atilde;o.</li>
    						</ul>
    
    						<p>
    							O ronco pode ocorrer de forma isolada ou estar associado &agrave; s&iacute;ndrome de apn&eacute;ia obstrutiva do sono (SAOS). A interrup&ccedil;&atilde;o abrupta do ronco durante alguns segundos pode indicar a ocorr&ecirc;ncia de um epis&oacute;dio de apn&eacute;ia, seguindo-se um novo ronco de maior intensidade, for&ccedil;ando a abertura da vida a&eacute;rea e reiniciando a respira&ccedil;&atilde;o.
    						</p>
    						
