                            <p class="subtitle">Perda Auditiva</p>
                            
							<p>Se voc&ecirc; tem uma perda auditiva, ou conhece algu&eacute;m que tenha, n&atilde;o est&aacute; sozinho. Pelo menos 25 milh&otilde;es de brasileiros t&ecirc;m diminui&ccedil;&atilde;o de audi&ccedil;&atilde;o.</p>

							<p>A surdez pode causar problemas emocionais e psicol&oacute;gicos, altera&ccedil;&otilde;es de aprendizado, altera&ccedil;&otilde;es de fala, problemas profissionais no trabalho, insatisfa&ccedil;&atilde;o e solid&atilde;o. Muitos idosos apresentam um quadro de Depress&atilde;o devido ao isolamento provocado pela defici&ecirc;ncia auditiva, pois n&atilde;o conseguem participar das conversas em grupo ou at&eacute; mesmo assistir televis&atilde;o.</p>

							<p>A maioria das pessoas com perda auditiva (em torno de 90%) podem ser ajudadas por meio de tratamento medicamentoso, cir&uacute;rgico ou de aparelhos de audi&ccedil;&atilde;o.</p>

							<p>Existem basicamente 2 tipos de surdez: a surdez de condu&ccedil;&atilde;o e a surdez do nervo auditivo ou da c&oacute;clea (surdez neurossensorial).</p>

							<p class="intro"><b>Surdez de condu&ccedil;&atilde;o</b></p>
							
							<p>A surdez de condu&ccedil;&atilde;o &eacute; aquela que afeta o ouvido externo ou m&eacute;dio e acontece quando as ondas sonoras n&atilde;o s&atilde;o bem conduzidas para o ouvido interno.Entre as causas est&atilde;o:</p>

							<ul>
								<li>Excesso de cera no ouvido</li>
								<li>Infec&ccedil;&otilde;es no ouvido: catarro no ouvido (Otite Secretora ou Otite Serosa), infec&ccedil;&otilde;es agudas do ouvido (Otite M&eacute;dia Aguda), perfura&ccedil;&atilde;o timp&acirc;nica, infec&ccedil;&otilde;es cr&ocirc;nicas do ouvido (Otite M&eacute;dia Cr&ocirc;nica)</li>
								<li>Imobiliza&ccedil;&atilde;o de um ou mais ossinhos do ouvido (Otosclerose)</li>
							</ul>

							<p>Normalmente, os problemas de surdez de condu&ccedil;&atilde;o podem ser resolvidos por tratamento m&eacute;dico (rem&eacute;dios) ou por cirurgia.</p>
								
							<p class="intro"><b>Tamp&atilde;o de cerumem (excesso de cera)</b></p>
							
							<p>A cera &eacute; uma produ&ccedil;&atilde;o normal da pele do canal externo de nosso ouvido. Ela serve para proteger a pele fina que reveste o canal do ouvido contra germes e subst&acirc;ncias que podem contamina-la ou feri-la. Al&eacute;m de proporcionar uma prote&ccedil;&atilde;o mec&acirc;nica ela possui lisoenzimas que mant&ecirc;m o Ph da pele. A cera de dentro do canal n&atilde;o deve ser retirada. Cera n&atilde;o &eacute; sujeira. Devemos somente retirar com uma toalha, por exemplo, a cera que aparece na orelha, mas por est&eacute;tica. N&atilde;o devemos introduzir nada dentro do canal do ouvido (cotonetes ou similares), pois al&eacute;m de tirar o revestimento normal de cera, ainda podemos empurr&aacute;-la mais para o fundo do ouvido, onde normalmente n&atilde;o tem cera, e entupi-lo.</p>

							<p>Existem pessoas que produzem muita cera a vida toda ou, em algumas fases da vida, chega a formar um tamp&atilde;o que impede a passagem do som. Outras vezes, n&atilde;o produzem muita cera, mas apresentam altera&ccedil;&otilde;es no canal auditivo que dificultam a elimina&ccedil;&atilde;o natural da cera.</p>
								
							<p>Este tamp&atilde;o ou "rolha" de cera s&oacute; pode ser removida por m&eacute;dico. N&atilde;o fa&ccedil;a lavagem ou remo&ccedil;&otilde;es em farm&aacute;cias ou com pessoas n&atilde;o habilitadas, pois  tais condutas podem causar problemas graves, tais como perfura&ccedil;&otilde;es no t&iacute;mpano ou infec&ccedil;&otilde;es.</p>

							<p class="intro"><b>Otite m&eacute;dia secretora ou serosa</b></p>

							<p>&Eacute; uma doen&ccedil;a que ocorre quando permanece catarro ou secre&ccedil;&atilde;o dentro do ouvido m&eacute;dio. Isto pode acontecer em virtude do mal funcionamento da tuba auditiva, quando se est&aacute; resfriado ou gripado, e se acumula catarro atr&aacute;s do t&iacute;mpano, ou quando a gente submete o ouvido a diferen&ccedil;as importantes de press&atilde;o (barotrauma durante v&ocirc;o ou mergulho).</p>

							<p>As crian&ccedil;as podem  n&atilde;o apresentar nenhum sintoma, pois elas muitas vezes n&atilde;o s&atilde;o capazes de referir esta sensa&ccedil;&atilde;o, mas a fam&iacute;lia ou a professora percebem que ela est&aacute; distra&iacute;da ou aumenta o volume da televis&atilde;o ou fala alto.</p>

							<p>Se este "entupimento" da tuba permanece, a secre&ccedil;&atilde;o que &eacute; formada no ouvido n&atilde;o tem por onde drenar e permanece dentro do ouvido m&eacute;dio, causando uma doen&ccedil;a chamada "otite secretora ou otite serosa". Em crian&ccedil;as, esta condi&ccedil;&atilde;o &eacute; bem comum quando h&aacute; associa&ccedil;&atilde;o de infec&ccedil;&otilde;es de vias a&eacute;reas superiores (gripes, amigdalites, otites, rinites, faringites).</p>

							<p>A otite &eacute; muito comum na crian&ccedil;a entre 2 a 8 anos e pode prejudicar o desenvolvimento da fala. Afinal, nesta faixa et&aacute;ria, &eacute; muito importante que a crian&ccedil;a ou&ccedil;a bem para poder falar bem e aprender. Muitas crian&ccedil;as com mau desenvolvimento escolar ou excesso de distra&ccedil;&atilde;o foram rotuladas como "crian&ccedil;as- problema", com altera&ccedil;&otilde;es psicol&oacute;gicas, mas tinham, na verdade, apenas uma otite serosa e n&atilde;o escutavam bem. Por isso, adotavam aquele comportamento.</p>

							<p>O tratamento da otite serosa cr&ocirc;nica, al&eacute;m de incluir a utiliza&ccedil;&atilde;o de medicamentos, tamb&eacute;m pode ser resolvida com procedimentos cir&uacute;rgicos, no caso de insucesso dos rem&eacute;dios. Esta cirurgia consiste em fazer uma pequena incis&atilde;o na membrana do t&iacute;mpano e colocar um dreno para entrar ar no ouvido.</p>

							<p class="intro"><b>Otosclerose</b></p>

							<p>A otosclerose ou otospongiose &eacute; uma causa comum de surdez e &eacute; heredit&aacute;ria. Normalmente, algu&eacute;m em sua fam&iacute;lia j&aacute; teve este problema, e algum de seus descendentes tamb&eacute;m pode vir a ter.</p>

							<p>Na otosclerose, o que ocorre s&atilde;o altera&ccedil;&otilde;es no osso chamado estribo que faz com que ele se imobilize e transmita menos o som para a c&oacute;clea. Estas altera&ccedil;&otilde;es tamb&eacute;m podem ocorrer em regi&otilde;es da c&oacute;clea, levando &agrave; surdez da c&oacute;clea.</p>

							<p>A otosclerose normalmente &eacute; mais comum em mulheres do que em homens, &eacute; rara na ra&ccedil;a negra, aparece normalmente na faixa et&aacute;ria entre 20 e 30 anos e piora quando a portadora fica gr&aacute;vida. Na maioria dos casos (70%), ela afeta um s&oacute; ouvido. Alguns casos podem ser acompanhados de zumbido (barulho no ouvido).</p>

							<p>&Eacute; importante destacar que a surdez &eacute; progressiva, isto &eacute;, vai piorando com o tempo, lenta ou rapidamente. Em alguns casos, a otosclerose pode ser tratada com cirurgia.</p>

							<p class="intro"><b>Surdez Neurossensorial</b></p>
								
							<p>&Eacute;  a forma mais comum de surdez.<br>As causas podem ser v&aacute;rias, desde problemas menores , como diminui&ccedil;&atilde;o na irriga&ccedil;&atilde;o sang&uuml;&iacute;nea do ouvido, at&eacute; mais s&eacute;rias, como tumores cerebrais. Estes problemas tamb&eacute;m ocorrem como parte do processo de nosso envelhecimento. A partir de 55 anos de idade, a audi&ccedil;&atilde;o pode come&ccedil;ar a diminuir, como acontece com a vis&atilde;o em idade menor ainda. Esta diminui&ccedil;&atilde;o normal da idade varia muito de pessoa para pessoa e est&aacute; normalmente ligada &agrave; heran&ccedil;a gen&eacute;tica, a condi&ccedil;&otilde;es anormais a que o ouvido foi exposto durante a vida (barulho intenso, infec&ccedil;&otilde;es etc...) ou a doen&ccedil;as gerais como hipertens&atilde;o arterial e diabete, que podem afetar o ouvido.</p>

							<p class="intro"><b>Causas da surdez do nervo</b></p>
							
							<p>Duas das mais comuns s&atilde;o:</p>
							
							<ul>
								<li>Exposi&ccedil;&atilde;o &agrave; ru&iacute;do de alta  intensidade ou sons altos</li>
								<li>Presbiacusia (surdez pela idade)</li>
							</ul>

							<p>Outras causas incluem:</p>
							
							<ul>
								<li>Viroses (rub&eacute;ola, caxumba)</li>
								<li>Meningite</li>
								<li>Uso de certos medicamentos ou drogas</li>
								<li>Propens&atilde;o familiar (heredit&aacute;rias)</li>
								<li>Traumas na cabe&ccedil;a</li>
								<li>Doen&ccedil;as c&aacute;rdio-circulat&oacute;rias</li>
								<li>Defeitos cong&ecirc;nitos</li>
								<li>Alergias</li>
								<li>Problemas metab&oacute;licos (diabetes, por exemplo)</li>
								<li>Tumores</li>
							</ul>

							<p>
								O diagn&oacute;stico precoce &eacute; crucial para poder controlar e tratar a maioria das causas da surdez. 
								Em muitos casos, n&atilde;o existe a cura do problema, por&eacute;m um aparelho auditivo restaura a audi&ccedil;&atilde;o. 
								&Eacute; como usar &oacute;culos para quem n&atilde;o enxerga. Os aparelhos auditivos tiveram grande avan&ccedil;o tecnol&oacute;gico nos &uacute;ltimos anos e hoje s&atilde;o de muito boa qualidade. 
								S&oacute; depende de uma boa escolha.Os aparelhos auditivos, assim como &oacute;culos, n&atilde;o curam a surdez, mas fazem a pessoa ouvir bem, 
								o que &eacute; o primeiro passo para o restabelecimento do bem-estar individual social, econ&ocirc;mico e emocional.
							</p>

							<p>Procure sempre um otorrinolaringologista se voc&ecirc; ou algu&eacute;m pr&oacute;ximo a voc&ecirc;:</p>

							<ul>
								<li>N&atilde;o entender direito o que as pessoas falam</li>
								<li>Usar o r&aacute;dio ou TV muito alto</li>
								<li>Pedir para repetir o que as pessoas falam</li>
								<li>Acusar que um ouvido escuta mais do que o outro</li>
								<li>Tiver que fazer esfor&ccedil;o para ouvir</li>
							</ul>

