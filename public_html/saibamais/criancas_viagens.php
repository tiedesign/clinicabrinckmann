                            <p class="subtitle">Crian&ccedil;as que passam mal durante as viagens</p>
                            
							<p>Algumas crian&ccedil;as ficam "enjoadas" e at&eacute; mesmo vomitam durante viagens de autom&oacute;veis ou de barco. As menores podem ficar inquietas, p&aacute;lidas ou chorar ao entrar no carro ou no barco.</p>
							
							<p>Isso ocorre devido a uma altera&ccedil;&atilde;o do funcionamento do labirinto (parte do ouvido que &eacute; respons&aacute;vel pelo equil&iacute;brio) conhecida como cinetose. A cinetose ocorre, na maioria das vezes, devido a uma imaturidade do labirinto. Na maioria das vezes, a cinetose n&atilde;o se associa a problemas graves, mas deve ser sempre avaliada pelo m&eacute;dico.</p>
							
							<p>Em algumas situa&ccedil;&otilde;es, pode-se usar um medicamento espec&iacute;fico para esse problema. Quando isso for necess&aacute;rio, &eacute; preciso oferecer o medicamento, sob orienta&ccedil;&atilde;o m&eacute;dica, pelo menos 30 minutos antes do in&iacute;cio da viagem. Em geral, a cinetose diminui ou desaparece com o crescimento.</p>

