								<li> Anterioriza&ccedil;&atilde;o &#8211; ex. chap&eacute;u-(sap&eacute;u); giz-(ziz) - at&eacute; os 4 anos;
								<li> Posterioriza&ccedil;&atilde;o &#8211; ex. sabe-(chabe); sapo-(chapo) - at&eacute; os 3 anos;
								<li> Plosiviza&ccedil;&atilde;o &#8211; ex. sopa-(topa); sapo-(tapo) &reg; at&eacute; os 2 anos e meio;
								<li> Substitui&ccedil;&atilde;o de /r/ ou /l/ por um /i/ - ex. bola-(boia); morango-(moiango); trator-(tratoi) - at&eacute; os 2 anos e meio.
							</ul>
							
							<p>
								Lembre-se ainda de que como a crian&ccedil;a aprende a linguagem ouvindo e falando, devemos conversar bastante com ela. 
								Aprende-se a falar em ambiente de fala! 
								A crian&ccedil;a tem condi&ccedil;&otilde;es inatas para, em pouco tempo, estabelecer elementos de comunica&ccedil;&atilde;o oral de constru&ccedil;&atilde;o surpreendentes.
							</p> 
							<p>
								A crian&ccedil;a &eacute; curiosa e usa a fala para entender o mundo, por isso est&aacute; sempre perguntando o por qu&ecirc; de tudo e n&atilde;o ficar&aacute; satisfeita com respostas do tipo: &#8220;porque &eacute;&#8221;, &#8220;porque sim&#8221;, ou &#8220;porque &eacute; assim mesmo&#8221;. 
								O &#8220;porqu&ecirc; do porqu&ecirc;&#8221; &eacute; constante e devemos saciar suas interroga&ccedil;&otilde;es, de forma objetiva e clara, sem enganar, ou prometer a resposta para depois. 
								Se o adulto n&atilde;o sabe como responder, &eacute; melhor dizer a verdade e procurar junto com a crian&ccedil;a a resposta. 
								Respostas ajudam a desenvolver a linguagem, o racioc&iacute;nio, o conhecimento e a intelig&ecirc;ncia em geral.
							</p>
							
							<p>
								O que a crian&ccedil;a recebe de sua fam&iacute;lia e educadores/as, mesmo sem a utiliza&ccedil;&atilde;o de palavras, pesa muito, tem muita valia na aquisi&ccedil;&atilde;o e no aperfei&ccedil;oamento da fala. 
							Aprender a falar &eacute;, tamb&eacute;m, aprender a armazenar palavras. 
							Mas lembre-se: o que &eacute; dito, &eacute; compreendido de acordo com o momento, o tom de voz que &eacute; proferido, a express&atilde;o facial e o gesto que acompanha.
							</p>
							<p>
								Um ambiente verbal adequado n&atilde;o &eacute; um simples despejar de palavras &#8211; cada palavra vem impregnada de muitos valores e sabores!
