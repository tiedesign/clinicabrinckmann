                            <p class="subtitle">Meu Filho Est&aacute; Falando Corretamente?</p>

							<p>
								Voc&ecirc; sabia que existe uma diferen&ccedil;a entre linguagem e fala? 
								O termo linguagem refere-se a um processo altamente complexo que est&aacute; diretamente relacionado &agrave; organiza&ccedil;&atilde;o do pensamento. 
								A linguagem possibilita estabelecer contato com os outros, compartilhar experi&ecirc;ncias, trocar id&eacute;ias e pensamentos, expressar desejos, enfim, comunicar-se. 
								J&aacute; a fala consiste na produ&ccedil;&atilde;o de sons atrav&eacute;s da interrup&ccedil;&atilde;o ou bloqueio da corrente de ar que vem dos pulm&otilde;es, ocasionada pelos movimentos dos l&aacute;bios, l&iacute;ngua e palato mole (final do c&eacute;u da boca).
							</p>
							<p>
								Para que ocorra um adequado desenvolvimento da fala e da linguagem &eacute; necess&aacute;rio que a crian&ccedil;a apresente capacidade intelectual e desenvolvimento cognitivo normal, bem como ser exposta a um ambiente encorajador e estimulante. &Eacute; fundamental ainda, que haja um desenvolvimento motor normal, assim como uma perfeita matura&ccedil;&atilde;o do sistema auditivo. 
								Portanto, a crian&ccedil;a deve apresentar sa&uacute;de f&iacute;sica, emocional e condi&ccedil;&otilde;es org&acirc;nicas adequadas para a produ&ccedil;&atilde;o da fala.
							</p>
							<p>
								&Eacute; principalmente pela linguagem que a crian&ccedil;a compreende e controla o seu mundo.
								Dando nome &agrave;s coisas e ao que faz &eacute; que a crian&ccedil;a descobre que pode pensar em objetos e pessoas que n&atilde;o est&aacute; vendo. 
								Melhor ainda, consegue at&eacute; imaginar situa&ccedil;&otilde;es.
							</p>
							<p>
								A fala &eacute; uma conquista psicol&oacute;gica e intelectual muito importante e que marca uma extraordin&aacute;ria acelera&ccedil;&atilde;o no desenvolvimento global da crian&ccedil;a.
							</p>
							<p>
								A palavra tem uma for&ccedil;a m&aacute;gica &#8211; o beb&ecirc; chama &#8220;mam&atilde;e&#8221; e ela aparece, pois &eacute; atrav&eacute;s da palavra que a crian&ccedil;a manifesta seus desejos. 
								Crian&ccedil;as pequenas que brincam juntas falam, mas n&atilde;o conversam e n&atilde;o se importam com isso; neste per&iacute;odo a palavra &eacute; parte do que ela faz.
							</p>
							<p>
								&Agrave; medida que a crian&ccedil;a se desenvolve, sua linguagem passa a ser usada para comunicar-se. 
								Ela come&ccedil;a a prestar aten&ccedil;&atilde;o no que os outros dizem e a conjugar os verbos &#8211; gramaticalmente incorretos, porque ela ouve &#8220;eu vi&#8221; e fala &#8220;eu di&#8221;, &#8220;eu fazi&#8221;, &#8220;eu pegui&#8221;, ou &#8220;eu janti&#8221;; desta forma ela est&aacute; tentando aplicar uma regra.
							</p>
							<p>
								&Eacute; importante respeitar a forma de falar da crian&ccedil;a nesse per&iacute;odo. 
								Apontar constantemente os seus &#8220;erros&#8221; poder&aacute; inibi-la, fazer com que ela fale pouco para cometer poucos erros. 
								Com isso, vai comunicar-se menos e, conseq&uuml;entemente, desenvolver-se mais lentamente. 
								Devemos repetir o que ela disse utilizando a palavra ou a frase de forma correta, mas sem dar &ecirc;nfase ao erro.
							</p>
							<p>
								Veja agora como ocorre o processo de desenvolvimento da linguagem e da fala:
							</p>
							
							<ul>
								<li> de 4 a 6 semanas: produ&ccedil;&atilde;o dos primeiros sons sem inten&ccedil;&atilde;o de comunica&ccedil;&atilde;o; choro curto e diferenciado;
								<li> de 3 a 4 meses: balbucio em resposta &agrave; estimula&ccedil;&atilde;o; risos espont&acirc;neos;
								<li> de 5 a 6 meses: balbucio por iniciativa pr&oacute;pria; brinca produzindo sons e risos; parece reconhecer palavras como mam&atilde;e, papai,tchau;
								<li> de 7 a 8 meses: parece escutar conversas longas, p&aacute;ra suas atividades quando o chamam; parece reconhecer o nome de objetos comuns quando solicitados;
								<li> de 9 a 10 meses: entrega objetos aos pais quando solicitado; mant&eacute;m interesse olhando uma figura; diz suas primeiras palavras: &#8220;papa&#8221;, &#8220;mama&#8221;, usa jarg&otilde;es (palavras inventadas);
								<li> de 1ano a 1 ano e meio: primeiras 50 palavras (12 meses = 3 palavras); entendem nomes de pessoas e objetos familiares; comunicam-se mais com jarg&atilde;o; compreendem mais do que falam; usam palavra-frase (por exemplo: &aacute;ga &#8211; para quero &aacute;gua);
								<li> de 1 ano e meio a 2 anos: vocabul&aacute;rio com 300 palavras; comunicam-se mais com palavras do que com jarg&atilde;o; utilizam a fala telegr&aacute;fica (formada por duas palavras); mostra partes do corpo e roupas quando solicitados; reconhece e identifica quase todos os objetos comuns;
								<li> de 2 anos a 3 anos: iniciam perguntas; falam frases com 2 ou 3 palavras; pedem ajuda quando precisam; come&ccedil;am a aprender as cores; repetem corretamente 2 ou mais n&uacute;meros; reconhecem o nome de objetos comuns em figuras; sabem diferenciar os sexos; explicam e nomeiam o que desenharam; come&ccedil;am a usar o plural; realizam 3 ordens simples;
								<li> de 3anos a 4 anos: produzem at&eacute; 90% das palavras adequadamente; vocabul&aacute;rio em torno de 500 a 1000 palavras; sua fala expressa pedido, pergunta, protesto; usam adequadamente frases negativas;
								<li> aos 5 anos a crian&ccedil;a j&aacute; deve ter presente todos os sons da fala, sem realizar trocas e t&atilde;o pouco omiss&otilde;es, incluindo a produ&ccedil;&atilde;o dos encontros consonantais como por exemplo: pedra, cobra, vidro, flor, flauta, etc.
							</ul>
							
							<p>
								Vale ressaltar que durante esse processo de desenvolvimento da fala algumas trocas e omiss&otilde;es podem ocorrer, mas devemos saber at&eacute; que idade aproximadamente essas altera&ccedil;&otilde;es podem ser consideradas normais. 
								Por isso aqui v&atilde;o alguns exemplos que s&atilde;o mais comumente observados:
							</p>
							
							<ul>
								<li>Apagamento de /s/ no final das s&iacute;labas &#8211; ex. escada-(i&ccedil;ada); l&aacute;pis-(l&aacute;pi) - at&eacute; os 3 anos e meio;
								<li> Apagamento do /r/ no final das s&iacute;labas &#8211; ex. barco-(baco); ventilador-(ventilado) - at&eacute; os 4 anos;
								<li> Apagamento do /R/ no in&iacute;cio da palavra &#8211; ex. Rua-(ua); Rabo-(abo) - at&eacute; os 2 anos e meio;
								<li> Apagamento do /r/ entre duas vogais &#8211; ex. jacar&eacute;-(jaca&eacute;); laranja-(laanja) - at&eacute; os 3 anos;
