                            <p class="subtitle">Boca Seca</p>
                            
    						<p>
    							Todos teremos "boca seca" pelo menos algumas vezes na vida, por exemplo: em estado de estresse, tristeza, ansiedade excessiva, etc. Mas ter "boca seca" o tempo todo pode ser desconfort&aacute;vel e ocasionar s&eacute;rios problemas de sa&uacute;de. Pode alterar o paladar, causar dificuldades na mastiga&ccedil;&atilde;o, na digest&atilde;o e na fala. Al&eacute;m disso, tamb&eacute;m predisp&otilde;e ao aparecimento de c&aacute;ries, aftas e  infec&ccedil;&otilde;es na boca.
    						</p>
    
    						<p>
    							Causas da Boca Seca:
    						</p>

                        	<ul>
                        		<li>Efeitos colaterais de alguns medicamentos;</li>
                        		<li>Doen&ccedil;as como, por exemplo, S&iacute;ndrome de Sj&ouml;gren, HIV/AIDS, diabetes, doen&ccedil;a de Parkinson;</li>
                        		<li>Radioterapia - tratamento de c&acirc;ncer;</li>
                        		<li>Quimioterapia - s&atilde;o drogas usadas no tratamento do c&acirc;ncer que deixam a saliva mais viscosa e espessa, causando a sensa&ccedil;&atilde;o de ressecamento;</li>
                        		<li>Les&atilde;o em nervos - inj&uacute;rias localizadas na cabe&ccedil;a e pesco&ccedil;o podem provocar les&otilde;es em nervos ligados &agrave;s gl&acirc;ndulas, provocando diminui&ccedil;&atilde;o da produ&ccedil;&atilde;o da saliva.</li>
                        	</ul>
    
    						<p>
                                O tratamento da "boca seca" depende da causa do problema. 
                                Por isso, &eacute; importante que o paciente procure o dentista ou m&eacute;dico para descobrir a origem do sintoma. 
                                Se for o uso de um medicamento, o m&eacute;dico pode tentar substitu&iacute;-lo ou ajustar melhor a dose.
    						</p>
    
    						<p>
    							Cuidados para evitar o ressecamento da boca:
    						</p>

                        	<ul>
                                <li>
                                    Prefira beber &aacute;gua ou bebidas sem a&ccedil;&uacute;car, principalmente durante as refei&ccedil;&otilde;es, 
