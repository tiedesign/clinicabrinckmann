                                </li>
                                <li>Tome cerca de 12 copos de l&iacute;quido por dia;</li>
                                <li>Evite bebidas cafeinadas, como caf&eacute;, alguns refrigerantes e ch&aacute;s;</li>
                                <li>Use sempre chicletes e balas sem adi&ccedil;&atilde;o de a&ccedil;&uacute;car para estimular o fluxo de saliva;</li>
                                <li>N&atilde;o use tabaco ou &aacute;lcool, pois ressecam a mucosa oral;</li>
                                <li>Lembre-se que alimentos muito salgados podem ocasionar dor em casos de "boca seca";</li> 
                                <li>Use um umidificador &agrave; noite, pois durante o sono n&atilde;o salivamos;</li>
                                <li>Procure corrigir a obstru&ccedil;&atilde;o nasal noturna porque respirar pela boca &agrave; noite piora o ressecamento;</li>
                                <li>Escove cuidadosamente os dentes, no m&iacute;nimo 2x/dia;</li>
                                <li>Use creme dental com fl&uacute;or;</li>
                                <li>Evite alimentos a&ccedil;ucarados ou grudentos na boca;</li>
                                <li>Visite o dentista regularmente.</li>
