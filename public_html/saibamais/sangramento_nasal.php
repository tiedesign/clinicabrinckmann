                            <p class="subtitle">Sangramento Nasal</p>
                            
							<p>As fossas nasais s&atilde;o estruturas amplamente irrigadas por uma complexa rede arterial e podem facilmente ser local de hemorragias.</p>

							<p>Existem v&aacute;rias afec&ccedil;&otilde;es que podem provocar o sangramento nasal. Dentre as causas sist&ecirc;micas, podemos citar: hipertens&atilde;o arterial n&atilde;o controlada, discrasias sang&uuml;&iacute;neas, neoplasias hematol&oacute;gicas, drogas (aspirinas, anticoagulantes, etc...), altera&ccedil;&otilde;es cardiovasculares, etc. Os fatores locais que podem ser causa de sangramento s&atilde;o: rinite descompensada, desvio septal, traumas nasais, corpo estranho, tumores nasais em geral, p&oacute;s-operat&oacute;rio de cirurgia nasal, irritantes qu&iacute;micos, dentre outros.</p>

							<p>O tratamento otorrinolarinl&oacute;gico para este tipo de problema, al&eacute;m da identifica&ccedil;&atilde;o e corre&ccedil;&atilde;o dos fatores desencadeantes, depende do local e da gravidade do caso: pode-se realizar uma simples cauteriza&ccedil;&atilde;o qu&iacute;mica do vaso sangrante ou um tamponamento nasal anterior em casos leves a moderados.</p>

							<p><b>O que um indiv&iacute;duo deve fazer frente a um sangramento nasal?</b></p>
							
							<ul>
								<li>Mantenha a calma e realize uma compress&atilde;o digital na narina sangrante, com a cabe&ccedil;a levemente inclinada para frente, pois se a mesma for inclinada para tr&aacute;s o sangue poder&aacute; escoar para a orofaringe;</li>
								<li>Evite qualquer tipo de calor pr&oacute;ximo &agrave; regi&atilde;o nasal: exposi&ccedil;&atilde;o solar, alimentos quentes, secador de cabelo, banhos quentes, ... </li>
								<li>Evite tamb&eacute;m fazer esfor&ccedil;os f&iacute;sicos;</li>
								<li>Fa&ccedil;a compressas geladas na regi&atilde;o nasal e lavagens nasais com soro fisiol&oacute;gico 0,9% frio, pois podem ajudar a evitar novos epis&oacute;dios;</li>
								<li>Em pacientes sem hist&oacute;ria de hipertens&atilde;o e cardiopatia, podemos realizar um tamponamento com algod&atilde;o embebido em vasoconstritor (Afrin, Sorine, ...) por 15 minutos.</li>
								<li>Caso o sangramento n&atilde;o acabe ap&oacute;s 5 a 10 minutos de compress&atilde;o digital, procure um pronto-socorro para atendimento m&eacute;dico especializado.</li>
							</ul>

