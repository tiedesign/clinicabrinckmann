                            <p class="subtitle">Reabilita&ccedil;&atilde;o Vestibular</p>
                            
    						<p>
    							Vestibular &eacute; um termo proveniente do nome Vest&iacute;bulo, que &eacute; um local dentro do ouvido interno, localizado no &oacute;rg&atilde;o que chamamos de labirinto. No sistema vestibular, existe um conjunto de estruturas sensoriais que permite que o nosso corpo se mantenha em equil&iacute;brio. Reabilita&ccedil;&atilde;o &eacute; todo tratamento que requer um treinamento espec&iacute;fico com o objetivo de restabelecer alguma perda de fun&ccedil;&atilde;o motora ou sensorial. Portanto, Reabilita&ccedil;&atilde;o Vestibular &eacute; o tratamento usado para treinamento do sistema vestibular com o objetivo de desenvolver o equil&iacute;brio do corpo.
    						</p>
    
    						<p>
    							As altera&ccedil;&otilde;es do equil&iacute;brio do corpo podem se manifestar de v&aacute;rias maneiras: sensa&ccedil;&atilde;o de tontura, desequil&iacute;brio, instabilidade, desvio da marcha ao andar, dificuldade de fixa&ccedil;&atilde;o do olhar, acompanhada ou n&atilde;o de n&aacute;useas ou v&ocirc;mitos. Entre as in&uacute;meras formas de tratamento, est&aacute; a Reabilita&ccedil;&atilde;o Vestibular, que atualmente tem sido considerada um dos mais efetivos m&eacute;todos para tratar o paciente que sofre de tontura.
    						</p>
    
    						<p>
                                V&aacute;rias t&eacute;cnicas podem ser usadas para na Reabilita&ccedil;&atilde;o Vestibular. 
                                Uma delas &eacute; a utiliza&ccedil;&atilde;o de exerc&iacute;cios f&iacute;sicos repetitivos, como movimentos de cabe&ccedil;a e olhos, movimento de tronco, brincadeiras com bola, andar com e sem movimento de cabe&ccedil;a e olhos, etc... - que s&atilde;o ensinados em consulta e praticados em casa, repetidas vezes, de forma disciplinada.
    						</p>
    
    						<p>
    							Antes de iniciarmos o tratamento propriamente dito, &eacute; necess&aacute;rio que o paciente participe de uma sess&atilde;o informativa de como funciona o labirinto, o efeito das doen&ccedil;as sobre o sistema vestibular e tamb&eacute;m, na mesma sess&atilde;o, aprenda a modificar alguns v&iacute;cios que surgem em conseq&uuml;&ecirc;ncia das crises vertiginosas. Notamos que, com essas orienta&ccedil;&otilde;es, os pacientes j&aacute; apresentam uma grande melhora dos sintomas.
    						</p>

                            <p>
                                <b>Atitudes Corretas</b>
                            </p>
                            
                            <p>
                                Inversamente ao que se pensa, fazer movimentos que provoquem a vertigem s&atilde;o recomendados para facilitar a melhora, tomando-se devidos cuidados para n&atilde;o provocar quedas ao ch&atilde;o. 
                                Inicialmente, recomenda-se faz&ecirc;-los sentado e, depois, em p&eacute;, se necess&aacute;rio com acompanhante ao lado, mas sem apoio. 
                                Pratique esportes, jogos com bola individuais (bola na parede, de uma m&atilde;o a outra, bola ao ch&atilde;o, etc...) ou em grupo, sempre fixando o olhar na bola.
                            </p>
                            
                            <p>
                                <b>Atitudes Incorretas</b>
                            </p>
                            
                            <p>
                                Acreditar que os movimentos que provocam a tontura dificultam a melhora ou pioram a doen&ccedil;a e, assim, evit&aacute;-los, enrijecendo a musculatura corporal, principalmente a do pesco&ccedil;o. 
                                Tamb&eacute;m n&atilde;o se deve abandonar atividades profissionais, caseiras, esportivas ou sociais, isolando-se por medo da crise. 
                                Evite andar apoiando-se em objetos ou pessoas, assim como sentar e levantar lentamente, usando apoio do bra&ccedil;o da cadeira ou sof&aacute;. 
                                N&atilde;o fique a maior parte do tempo deitado, parado, sem atividade alguma.
                            </p>
                                    						    						
