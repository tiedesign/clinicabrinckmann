                            <p class="subtitle">Otite do nadador - dor de ouvido nas f&eacute;rias de ver&atilde;o</p>
                            
							<p>As otites do nadador (infec&ccedil;&atilde;o da pele do canal auditivo externo) s&atilde;o resultado da perman&ecirc;ncia de &aacute;gua, por tempo prolongado, no canal auditivo. Por isso, &eacute; importante, naquelas crian&ccedil;as predispostas a essa infec&ccedil;&atilde;o, secar o ouvido ap&oacute;s a nata&ccedil;&atilde;o. Isto pode ser feito pelos pais, usando o secador de cabelo manual em temperatura morna, posicionado a uma dist&acirc;ncia adequada da orelha. Deve-se evitar o uso de gotas com &aacute;lcool.</p>
							
