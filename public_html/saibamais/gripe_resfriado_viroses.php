                            <p class="subtitle">Gripe, resfriado e viroses</p>
                            
							<p>Os v&iacute;rus s&atilde;o micro-organismo que se modificam com muita freq&uuml;&ecirc;ncia; assim como a moda, que a cada ano est&aacute; diferente!</p>

							<p>Muitos v&iacute;rus causam doen&ccedil;as das vias respirat&oacute;rias.</p>

							<p>Essas doen&ccedil;as s&atilde;o as conhecidas viroses.</p>

							<p>As crian&ccedil;as t&ecirc;m mais doen&ccedil;as causadas por v&iacute;rus porque o sistema imunol&oacute;gico (sistema de defesa) delas ainda n&atilde;o amadureceu.</p>

							<p>Quando as crian&ccedil;as s&atilde;o menores, as viroses podem atingir as vias a&eacute;reas e as vias digestivas, causando manifesta&ccedil;&otilde;es no nariz (secre&ccedil;&atilde;o, obstru&ccedil;&atilde;o), na garganta (d0r, pigarro), na traqu&eacute;ia, nos pulm&otilde;es (tosse e secre&ccedil;&atilde;o) e intestino (diarr&eacute;ia e v&ocirc;mitos).</p>

							<p>Os resfriados e as gripes s&atilde;o as viroses mais "famosas" que atacam as vias a&eacute;reas e s&atilde;o causados por in&uacute;meros v&iacute;rus. O principal v&iacute;rus causador da "gripe" &eacute; o Influenza. A vacina contra a gripe protege cerca de 70% das crian&ccedil;as que foram vacinadas. A vacina&ccedil;&atilde;o deve ser repetida anualmente, j&aacute; que este v&iacute;rus fica diferente a cada ano.</p>

							<p>As principais altera&ccedil;&otilde;es causadas pelos v&iacute;rus s&atilde;o: nariz entupido e/ou escorrendo, olhos avermelhados, febre, dor no corpo e tosse.</p>

							<p>A gripe &eacute; sempre mais intensa que os resfriados: a febre &eacute; mais alta e o corpo d&oacute;i mais! A chance de complica&ccedil;&otilde;es, como a pneumonia, as otites e as sinusites, &eacute; maior.</p>

							<p>Uma crian&ccedil;a pode ter cerca de seis viroses por ano (resfriados). E, se j&aacute; estiver numa escolinha, tem maior chance de contrair v&iacute;rus.</p>

							<p>O tratamento das viroses &eacute; feito com hidrata&ccedil;&atilde;o, analg&eacute;sicos e antit&eacute;rmicos adequados, al&eacute;m de certo repouso.</p>

							<p>O acompanhamento m&eacute;dico pode evitar as complica&ccedil;&otilde;es das viroses: sinusites, otites ou pneumonias.</p>
        						    						
