                            <p class="subtitle">Fonoaudiologia e Sa&uacute;de Vocal</p>

							<p>Fonoaudiologia, palavra dif&iacute;cil ainda, para muitos um tanto desconhecida, para outros a possibilidade de uma comunica&ccedil;&atilde;o mais eficaz. Esta ci&ecirc;ncia atua em pesquisa, preven&ccedil;&atilde;o, avalia&ccedil;&atilde;o e tratamento dos dist&uacute;rbios da comunica&ccedil;&atilde;o humana. Sabemos que uma perfeita comunica&ccedil;&atilde;o necessita de boa audi&ccedil;&atilde;o, boa  voz, fala fluente e bom racioc&iacute;nio.</p>
						
							<p>A voz e a fala  s&atilde;o produzidas por grupos musculares  que necessitam de repouso, alongamento e  fortalecimento. Rotineiramente passamos a valorizar a voz apenas quando experimentamos um dist&uacute;rbio vocal. Com a experi&ecirc;ncia de n&atilde;o podermos falar, damos maior aten&ccedil;&atilde;o a este recurso t&atilde;o maravilhoso. O que seria dos cantores, professores, empres&aacute;rios, pastores, locutores, apresentadores, radialistas, vendedores, telefonistas e at&eacute; mesmo dos pol&iacute;ticos se n&atilde;o possu&iacute;ssem boa capacidade de comunica&ccedil;&atilde;o?</p>
						
							<p>A vida moderna exige que todos sejam bons comunicadores independente da &aacute;rea de atua&ccedil;&atilde;o profissional. &Eacute; atrav&eacute;s da fala e da voz que somos caracterizados e descritos. Ao falarmos em tom natural e velocidade ideal demonstramos controle e dom&iacute;nio sobre o assunto, por outro lado se falarmos forte e r&aacute;pido demais podemos demonstrar descontrole emocional.</p>
						
							<p>Caso voc&ecirc; desconfie que tenha algum problema de voz, fique atento:</p>
						
							<ul>
								<li>Rouquid&atilde;o por mais de dez ou quinze dias deve ser avaliada por um otorrinolaringologista;</li>
								<li>Outros sintomas como: rouquid&atilde;o freq&uuml;ente, perda de voz ao final do dia, instabilidade da voz, presen&ccedil;a de pigarro, necessidade de tossir ou limpar a garganta constantemente s&atilde;o sinais de que sua sa&uacute;de vocal necessita cuidados;</li>
								<li>Problemas respirat&oacute;rios como rinites e sinusites podem alterar a qualidade da voz e da fala de adolescentes e adultos. Em alguns casos s&atilde;o os causadores de desvios no padr&atilde;o de crescimento da face e das arcadas dent&aacute;rias, resultando em  altera&ccedil;&otilde;es dent&aacute;rias e articulat&oacute;rias dos sons da fala desde a inf&acirc;ncia.</li>
							</ul>
						
							<p>
								Agora se voc&ecirc; n&atilde;o sofre de nenhum desconforto citado acima, por&eacute;m gostaria de falar melhor, saiba que todos podemos aprimorar nossas habilidades de comunica&ccedil;&atilde;o. 
								Assim afastaremos o receio de expor nossas id&eacute;ias, conquistaremos nosso espa&ccedil;o nas rela&ccedil;&otilde;es interpessoais e seremos pessoas mais realizadas socialmente e profissionalmente. 
								Portanto, cuide bem de sua comunica&ccedil;&atilde;o, converse com um fonoaudi&oacute;logo!
							</p>
						
							<p class="intro"><strong>H&aacute;bitos prejudiciais &agrave; voz</strong></p>
							
							<ul>
								<li>Fumar;</li>
								<li>Ingerir bebida alc&oacute;olica  (principalmente as destiladas);</li>
								<li>Falar muito durante gripes, resfriados ou alergias do aparelho respirat&oacute;rio</li>
								<li>Falar em ambientes ruidosos;</li>
								<li>Permanecer em locais com ar condicionado por longos per&iacute;odos;</li>
								<li>Auto-medica&ccedil;&atilde;o (pastilhas, gengibre, sprays...)</li>
							</ul>
						
							<p class="intro"><strong>Dicas para ter uma boa voz</strong></p>
							
							<ul>
						        <li>Beber pelo menos 2 litros de &aacute;gua (8 a 10 copos) por dia;</li>
						        <li>Articular bem as palavras abrindo bem a boca;</li> 
						        <li>Ao utilizar a voz como recurso de trabalho, realizar exerc&iacute;cios espec&iacute;ficos (indicados por um especialista);</li> 
						        <li>Reconhecer as sensa&ccedil;&otilde;es de desgaste e evitar situa&ccedil;&otilde;es de esfor&ccedil;o;</li> 
						        <li>Realizar acompanhamento m&eacute;dico peri&oacute;dico.</li> 
							</ul>

                                                       
